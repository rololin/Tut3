import android.widget.ArrayAdapter;
import android.widget.ListAdapter;
import android.widget.ListView;
import android.widget.Toast;


public class MainActivity extends ActionBarActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);


     String[] TVShows ={"Apple","BABA","Charlie","Donkey","Elephant","Frank","Google","Happy","India","Japan","Korea","London"};

    ListAdapter the_adapter = new MyAdapter(this,TVShows);

    ListView theListView = (ListView) findViewById(R.id.listView);
    theListView.setAdapter(the_adapter);

    theListView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
        @Override
        public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
            String TvShowpicked = "You Selected " + String.valueOf(parent.getItemAtPosition(position).toString());
            Toast.makeText(MainActivity.this,TvShowpicked,Toast.LENGTH_SHORT).show();
        }
    });
    }


    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.menu_main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }
}
